
# Arithimatic operators

x = int(input("Enter First Number : "))
y = int(input("Enter Second Number : "))

plus = x + y 

print(f"The sum of {x} and {y} is = {plus}")

sub = x - y
print(f"The substraction of {x} and {y} is = {sub}")

multi = x * y
print(f"The multiplacation of {x} and {y} is = {multi}")

div = x / y
print(f"The division of {x} and {y} is = {div}")

mod = x % y 
print(f"The modules of {x} and {y} is = {mod}")

exp = x ** y
print(f"The exponentiation of {x} and {y} is = {exp}")

fdiv = x // y
print(f"The floor division of {x} and {y} is = {fdiv}")


