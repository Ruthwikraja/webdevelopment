#Check if a Substring is Present in a Given String

#Algorithm :-

#step1 :- create string
#step2 :- Again create substring
#step3 :- when if condition (string.count(substring)>0)
#step4 :- print the statement substring is present in string
#step5 :- else part otherwise substring is not present in string
#step6 :- Exit

#Program

string = input("Enter the String :-")
substring = input("Enter the substring :-")

if (string.count(substring) > 0) :
    print(f" The Substring is present in string")
else :
    print(f" The Substring is not present in string")

    








