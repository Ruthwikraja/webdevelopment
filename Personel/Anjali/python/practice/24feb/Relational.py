#Relational operator
num1=int(input("Enter the number"))
num2=int(input("Enter the number"))

#==operator
print(f"{num1} == {num2} = ",num1==num2)

#!=operator
print(f"{num1} != {num2} = ",num1!=num2)

#<operator
print(f"{num1} < {num2} = ",num1<num2)

#>operator
print(f"{num1} > {num2} = ",num1>num2)

#<=operator
print(f"{num1} <= {num2} = ",num1<=num2)

#>=operator
print(f"{num1} >= {num2} = ",num1>=num2)