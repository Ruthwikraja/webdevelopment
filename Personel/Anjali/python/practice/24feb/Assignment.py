#Assignment Operators
num1=int(input("Enter the number"))
num2=int(input("Enter the number"))

# += operator
num1 += num2
print(num1)

# -= operator
num1 -= num2
print(num1)

# *= operator
num1 *= num2
print(num1)

# /= operator
num1 /= num2
print(num1)

# //= operator
num1 //= num2
print(num1)

# **= operator
num1 **= num2
print(num1)