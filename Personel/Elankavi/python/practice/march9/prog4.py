# Escape character

a=input("Enter your name : ")
print(f"your name is \"{a}\" ")
# to print the new line 
print(f"your name is \n \"{a}\" ")
#in single quotes
print(f"your name is \'{a}\' ")
#backspace 
print(f"your name is  \b{a}\b ")
#form feed
print(f"your name is \f{a}\f ")
#carrige return
print(f"your name is \r{a}\r ")
#Tab
print(f"your name is \t{a}\t ")
