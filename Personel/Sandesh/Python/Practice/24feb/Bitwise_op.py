A = int(input("Enter a number :"))
B = int(input("Enter a number :"))

#Bitwise And
C = A & B;   
print ("Bitwise And = ", C)

#Bitwise OR
C = A | B;    
print ("Bitwise OR= ", C)

#Bitwise XOR
C = A ^ B;    
print ("Bitwise XOR = ", C)

#Binary's ones Complement
C = ~A;     
print ("One's Complement = ", C)

#Binary fill left shift
C = A << 2;  
print ("Left Shift = ", C)

#Binary right shift
C = A >> 2;   
print ("Right Shift = ", C)