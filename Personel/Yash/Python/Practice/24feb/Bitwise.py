x = int(input("Enter the first number : "))
y = int(input("Enter the second number : "))

#AND
print("x & y : ",x & y)

#OR
print("x | y : ",x | y)

#XOR
print("x ^ y : ",x ^ y)

#NOT(negation)
print("~x : ",~x," ~y : ",~y)

#RIGHT SHIFT
print("x >> 2 : ",x>>2," y>>2 : ",y>>2)

#LEFT SHIFT
print("x << 2 : ",x<<2," y<<2 : ",y<<2)