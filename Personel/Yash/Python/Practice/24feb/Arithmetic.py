x = int(input("Enter the first number : "))
y = int(input("Enter the second number : "))

#Addition
print("x + y =",x+y) 

# Subtraction
print("x - y = ",x-y) 

#Multiplication
print("x * y = ",x*y)

#Division
print("x / y = ",x/y)

#Modulus
print("x % y = ",x%y)

#Exponentiation
print("x ** y = ",x**y)

#Floor division
print("x // y = ",x//y)