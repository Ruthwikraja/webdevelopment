#Common Python String Methods
#Lower Case
print('---LOWER CASE---')
a='RAJKUMAR VARMA'.lower()
print(a)
print()
#Upper Case
print('---UPPER CASE---')
a='rajkumar varma'.upper()
print(a)
print()
#Split method
print('---UPPER CASE---')
a='I AM RAJKUMAR VARMA'.split()
print(a)
print()
#Join Method
print('---UPPER CASE---')
a=' '.join(['I', 'AM', 'RAJKUMAR', 'VARMA'])
print(a)
print()
#find method
a='I AM RAJKUMAR VARMA'.find('AM')
print(a)
print()
#Replace method
a='I AM RAJKUMAR VARMA'.replace('RAJKUMAR','SHIVKUMAR')
print(a)



