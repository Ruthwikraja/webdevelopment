#Comparison Operators


A = int(input("First Number : "))
B = int(input("Second Number : "))

#Equal 
print(f"{A} == {B} is = ",A > B) 


#Not Equal
print(f"{A} != {B} is = ",A > B)


#Greater than
print(f"{A} > {B} is = ",A > B)

#Less than
print(f"{A} < {B} is = ",A > B)


#Greater than or equal to
print(f"{A} >= {B} is = ",A > B)


#Less than or Equal to
print(f"{A} <= {B} is = ",A > B)

