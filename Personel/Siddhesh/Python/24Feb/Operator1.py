# Arithmetic operators 

num1 = int(input("Enter the number," ))
num2 = int(input("Enter the Number, "))


# Addition operator(+)
Add = num1 + num2
print(f"The sum of {num1} and {num2} is = {Add}")


# substraction operator(-)
Sub = num1 + num2
print(f"The sub of {num1} and {num2} is = {Sub}")


# Multiplication operator(*)
Mul = num1 * num2
print(f"The Multiplication of {num1} and {num2} is = {Mul}")


# Division operator(/)
Div = num1 / num2
print(f"The division of {num1} and {num2} is = {Div}")

# Modulus operator(%)
Mod = num1 % num2
print(f"The Modulus of {num1} and {num2} is = {Mod}")


# Exponentiation operator(**)
Power = num1 ** num2
print(f"The Exponentiation of {num1} and {num2} is = {Power}")



# Floor division operator(//)
floordiv = num1 // num2
print(f"The Floor division of {num1} and {num2} is = {floordiv}")

