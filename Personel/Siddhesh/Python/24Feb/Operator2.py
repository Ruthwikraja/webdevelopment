#Assignment Operators

x = int(input("Enter the Number : "))
y = int(input("Enter the Number : "))
z = 0


print(f"Assigning the value to x={x} and y={y}")

z = x + y
print(" Value of z is ",z)

z += x
print(" Value of z is ",z)

z -= y
print(" Value of z is ",z)

z *= x
print(" Value of z is ",z)


z  /= y
print(" Value of z is ",z)

z %= x
print(" Value of z is ",z)

z **= y
print(" Value of z is ",z)

z //= x
print(" Value of z is ",z)



