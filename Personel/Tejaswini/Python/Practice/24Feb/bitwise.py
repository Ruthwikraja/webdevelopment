a = int(input("Enter First No.:"))
b = int(input("Enter Second No.:"))

c = a & b;   
print (f"Bitwise And = {c}")

c = a | b;    
print (f"Bitwise OR= {c}")

c = a ^ b;    
print (f"Bitwise XOR = {c}")

c = ~a;     
print (f"One's Complement= {c}")

c = a << 2;  
print (f"Left Shift= {c}")

c = a >> 2;   
print (f"Right Shift= {c}")

